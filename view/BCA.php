                    <h3> Introduction </h3>
                    <p>Tribhuvan University has launched Bachelor of Computer Application (BCA) program from the academic year 2074/75. In the first phase, this program was launched in six (6) constituent campuses of Tribhuvan University and are allocated 35 seats each.

                        Currently 120 colleges are running BCA program with the affiliation from Tribhuvan University. The BCA program of TU is of 4 years. The program runs on semester-system. It will be run under the faculties of Humanities and Social Sciences.

                        The objective of the BCA program of Tribhuvan University is to produce high quality computer application users and developers. The program of study for Bachelor of Arts in Computer Application (BCA) is over a period of eight semesters (four academic years). The academic year begins in the September and February of each year. The medium of instruction and examination in the Bachelor of Arts in Computer Application (BCA) program is English.</p>
